#!/usr/bin/env python3
"""
Explore Valcre workbook to find where boilerplate text is stored.
Look for certification statements, reconciliation paragraphs, and other template text.
"""

import openpyxl
from pathlib import Path

WORKBOOK_PATH = Path("/Users/bencrowe/Development/APR-Dashboard-v3/docs/15-Contract-review/VAL251012 - North Battleford Apt, 1101, 1121 109 Street, North Battleford.xlsm")

print("=" * 80)
print("EXPLORING WORKBOOK FOR BOILERPLATE TEXT")
print("=" * 80)

# Load workbook
print("\n[1] Loading workbook...")
wb = openpyxl.load_workbook(WORKBOOK_PATH, data_only=True)
print(f"✓ Loaded {len(wb.sheetnames)} sheets")

# Look for certification text in all sheets
print("\n[2] Searching for 'certification' text...")
certification_locations = []

search_terms = [
    "certify that",
    "statements of fact",
    "CUSPAP",
    "reconciliation",
    "cost approach has limited applicability",
    "income generating asset"
]

for sheet_name in wb.sheetnames:
    sheet = wb[sheet_name]

    # Only search first 500 rows to be reasonable
    for row in range(1, min(500, sheet.max_row + 1)):
        for col in range(1, min(50, sheet.max_column + 1)):
            cell = sheet.cell(row, col)
            if cell.value and isinstance(cell.value, str):
                # Check if any search terms match
                for term in search_terms:
                    if term.lower() in cell.value.lower():
                        certification_locations.append({
                            'sheet': sheet_name,
                            'cell': f"{cell.column_letter}{cell.row}",
                            'term': term,
                            'value': cell.value[:200]  # First 200 chars
                        })
                        break

print(f"✓ Found {len(certification_locations)} potential boilerplate text locations")

# Print findings
print("\n[3] BOILERPLATE TEXT LOCATIONS:")
print("=" * 80)

for loc in certification_locations:
    print(f"\nSheet: {loc['sheet']}")
    print(f"Cell:  {loc['cell']}")
    print(f"Term:  {loc['term']}")
    print(f"Text:  {loc['value']}")
    print("-" * 80)

# Check specific named ranges for certification
print("\n[4] Checking named ranges for certification/reconciliation...")

cert_ranges = []
for name in wb.defined_names.keys():
    if any(keyword in name.lower() for keyword in ['cert', 'recon', 'boiler', 'text', 'para']):
        try:
            defn = wb.defined_names[name]
            if defn.destinations:
                sheet_name, cell_ref = list(defn.destinations)[0]
                sheet = wb[sheet_name]
                cell = sheet[cell_ref.replace('$', '')]
                value = cell.value
                if value:
                    cert_ranges.append({
                        'name': name,
                        'sheet': sheet_name,
                        'cell': cell_ref,
                        'value': str(value)[:200]
                    })
        except:
            pass

print(f"✓ Found {len(cert_ranges)} named ranges with certification/reconciliation keywords")

for rng in cert_ranges:
    print(f"\nNamed Range: {rng['name']}")
    print(f"Sheet:       {rng['sheet']}")
    print(f"Cell:        {rng['cell']}")
    print(f"Value:       {rng['value']}")
    print("-" * 80)

# Summary
print("\n" + "=" * 80)
print("SUMMARY")
print("=" * 80)
print(f"Total text locations found: {len(certification_locations)}")
print(f"Total named ranges found:   {len(cert_ranges)}")
print("\nNext step: Review the locations above to see if boilerplate is:")
print("  1. Stored in specific named ranges (easy to extract)")
print("  2. Hard-coded in specific cells (need manual mapping)")
print("  3. Generated by VBA macros (need to check VBA code)")
print("=" * 80)
